import Foundation
import UIKit


class UIDesignClass {
    
    func addButtonStyling(btn : UIButton){
        btn.layer.borderWidth = 2
        btn.layer.borderColor = UIColor.systemGreen.cgColor
        btn.layer.cornerRadius = 10
    }
    
}
